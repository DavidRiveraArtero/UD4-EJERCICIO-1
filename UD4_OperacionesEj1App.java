public class UD4_OperacionesEj1App {

	public static void main(String[] args) {
		int a=20;
		
		int b=30;
		
		//PRINTAMOS LA SUMA
		System.out.println(a + b);
		//PRINTAMOS LA RESTA
		System.out.println(a - b);
		//PRINTAMOS LA MULTIPLICACION 
		System.out.println(a * b);
		//PRINTAMOS LA DIVISION
		System.out.println(a / b);
		//PRINTAMOS EL MODULO
		System.out.println(a % b);

	}

}
